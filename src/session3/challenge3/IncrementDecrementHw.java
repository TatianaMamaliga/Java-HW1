package session3.challenge3;

public class IncrementDecrementHw {
    public static void main(String[] args) {
        //Create a Java program where you increment and decrement the same variable. Display the value of the variable after each operation.
        int number = 6;
        System.out.println("Initial number is: " + number);
        number++;
        System.out.println("Result after incrementing: " + number);
        number--;
        System.out.println("Result after decrementing: " + number);
    }
}
