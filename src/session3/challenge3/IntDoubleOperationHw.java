package session3.challenge3;

public class IntDoubleOperationHw {

    public static void main(String[] args) {
        int firstNum = 2;
        double secondNum = 5.5;

        System.out.println("Sum result: " + (firstNum + secondNum));
        System.out.println("Subtraction result: " + (firstNum - secondNum));
        System.out.println("Division result: " + (firstNum / secondNum));
        System.out.println("Multiplication result: " + (firstNum * secondNum));
    }
}
