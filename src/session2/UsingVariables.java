package session2;

public class UsingVariables {
    //Write a program that defines four different variables (an integer, a double, a character, and a string), assigns them values, and then prints them out.
    public static void main(String[] args) {
        int num = 50;
        double doubleNum = 60.99;
        char letter = 'X';
        String word = "Tesla";

        System.out.println("the integer is: " + num);
        System.out.println("the double is: " + doubleNum);
        System.out.println("the character is: " + letter);
        System.out.println("the string is: " + word);
    }
}
